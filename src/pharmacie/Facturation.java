    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        Date sqldate = new Date();
        java.sql.Date date = new java.sql.Date(sqldate.getTime());

        SimpleDateFormat formater = new SimpleDateFormat("dd-MM-yy");
        
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("select * from facturation where facturation.date='"+formater.format(date)+"'");
            table.setAutoResizeMode(table.AUTO_RESIZE_OFF);
            table.setModel(DbUtils.resultSetToTableModel(rs));          
        }
        catch(Exception e){ 
            JOptionPane.showMessageDialog(null, "Erreur de connexion");
        }
    }//GEN-LAST:event_formComponentShown

