        java.sql.Date date = new java.sql.Date(sqldate.getTime());

        SimpleDateFormat formater = new SimpleDateFormat("dd-MM-yy");
        
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("insert into facturation values('"+patientID+"', '"+numeroFacture+"', '"+service+"', '"+total+"', '"+refference+"', '"+nom+"', '"+prenom+"', '"+age+"', '"+adresse+"', '"+agent+"', '"+formater.format(date)+"')");
            JOptionPane.showMessageDialog(null, "Actualiser avec succée!!");
            setVisible(false);
            new Facturation().setVisible(true);
        }
